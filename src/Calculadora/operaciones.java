public class operaciones {
    
    public float resultado;
    
    public void sumar(float n1, float n2 ) {
           
        resultado = n1 + n2;
 
}
    public void restar(float n1, float n2 ) {
           
        resultado = n1 - n2;
 
}
    public void dividir(float n1, float n2 ) {
           
        resultado = n1 / n2;
 
}
    public void multiplicar(float n1, float n2 ) {
           
        resultado = n1 * n2;
 
}
    
    
}
